def classify_project(name, description):
    """Classify project into focus areas based on name + description."""
    text = (name + " " + description).lower()
    matched = []
    for area, keywords in FOCUS_AREAS.items():
        for kw in keywords:
            if kw.lower() in text:
                matched.append(area)
                break
    return list(set(matched)) if matched else ["Other"]


def rank_trending_repos(repos, min_stars=100):
    """Rank repos by star count, group by focus area."""
    ranked = sorted([r for r in repos if r["stars"] >= min_stars], key=lambda x: x["stars"], reverse=True)
    by_area = {area: [] for area in list(FOCUS_AREAS.keys()) + ["Other"]}
    for repo in ranked:
        areas = classify_project(repo["name"], repo["description"])
        for area in areas:
            if area in by_area and len(by_area[area]) < 10:
                by_area[area].append(repo)
    return ranked, by_area


def generate_markdown(today, repos, filtered_repos):
    """Generate markdown report."""
    ranked, by_area = rank_trending_repos(repos)

    lines = [
        f"# GitHub Trending Daily Report",
        f"**Date:** {today}",
        "",
        "## 📊 Summary",
        f"- Total trending repos analyzed: **{len(repos)}**",
        f"- Repos in target languages (Python/JS/TS/Rust/Go): **{len(filtered_repos)}**",
        "",
        "## 🔥 Top Stars Today",
        "",
    ]

    for repo in ranked[:10]:
        lang = f" `{repo['language']}`" if repo["language"] else ""
        stars = f"⭐ {repo['stars']:,}" + (f" (+{repo['today_stars']} today)" if repo["today_stars"] else "")
        lines.append(f"### [{repo['name']}](https://github.com/{repo['name']})")
        lines.append(f"{stars}{lang}")
        if repo["description"]:
            lines.append(f"> {repo['description']}")
        lines.append("")

    lines += ["## 📂 By Focus Area", ""]
    for area, area_repos in by_area.items():
        if area_repos:
            lines.append(f"### {area}")
            for repo in area_repos[:5]:
                lang = f" `{repo['language']}`" if repo["language"] else ""
                lines.append(f"- **[{repo['name']}](https://github.com/{repo['name']})** ⭐{repo['stars']:,}{lang} — {repo['description'][:80]}")
            lines.append("")

    lines += [
        "## 🕐 Notes",
        "- Report generated automatically by GitHub Researcher",
        "- Data source: [GitHub Trending](https://github.com/trending)",
        "- Analysis covers: Python, JavaScript, TypeScript, Rust, Go",
        "",
        "---",
        "*Auto-generated by [github-researcher](https://github.com/appcanmr/github-researcher)*",
    ]

    return "\n".join(lines)


