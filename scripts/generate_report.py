    ranked, by_area = rank_trending_repos(repos, min_stars=50)

    sections = [f"## Trend Index\n\n*Last updated: {today}*\n"]
    sections.append("### Top 20 All-Time (from recent data)\n")
    for repo in ranked[:20]:
        sections.append(f"- [{repo['name']}](https://github.com/{repo['name']}) ⭐{repo['stars']:,} `{repo['language'] or 'N/A'}` — {repo['description'][:60]}")

    for area, area_repos in by_area.items():
        if area_repos:
            sections.append(f"\n### {area}\n")
            for repo in area_repos[:10]:
                sections.append(f"- [{repo['name']}](https://github.com/{repo['name']}) ⭐{repo['stars']:,}")

    Path("indexes/trend-index.md").write_text("\n".join(sections), encoding="utf-8")
